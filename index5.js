function displayComputers(computers) {
    const computersList = document.getElementById('computers-list');
    computersList.innerHTML = ''; // Очистить текущий список
    computers.forEach(computer => {
        const div = document.createElement('div');
        div.className = 'computer';
        div.innerHTML = `
            <img src="${computer.img}" alt="${computer.name}">
            <h3>${computer.name}</h3>
            <p>${computer.specs}</p>
        `;
        computersList.appendChild(div);
    });
}

document.getElementById('search').addEventListener('input', function(event) {
    const query = event.target.value.toLowerCase();
    const filteredComputers = [];
    for (let brand in computersData) {
        const brandComputers = computersData[brand].filter(computer => 
            computer.name.toLowerCase().includes(query)
        );
        filteredComputers.push(...brandComputers);
    }
    displayComputers(filteredComputers);
});

function showComputers(brand) {
    const computers = computersData[brand] || [];
    displayComputers(computers);
}



